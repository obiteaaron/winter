package tech.obiteaaron.winter.embed.schedulercenter.exception;

public class WinterSchedulerMapException extends WinterSchedulerException {
    public WinterSchedulerMapException() {
        super();
    }

    public WinterSchedulerMapException(String message) {
        super(message);
    }

    public WinterSchedulerMapException(String message, Throwable cause) {
        super(message, cause);
    }

    public WinterSchedulerMapException(Throwable cause) {
        super(cause);
    }

    protected WinterSchedulerMapException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
